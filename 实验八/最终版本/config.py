import numpy as np

# ------------------------------------------------------------------
# 网络规模与时间参数
# ------------------------------------------------------------------
N = 2000                        # 输入神经元总数
T_sim = 450 * 1000              # 主仿真时长：450 秒（单位：ms）
dt = 1                          # 仿真步长：1 ms
T_pattern = 50                  # 模式模板时长：50 ms

# ------------------------------------------------------------------
# 输入脉冲参数
# ------------------------------------------------------------------
r_bg = 52.0                     # 固定背景发放率 (Hz)
r_spont = 10.0                  # 自发活动发放率 (Hz)
pattern_freq = 0.25             # 模式占空比（模式出现频率）
N_pattern_neurons = N // 2       # 参与模式的神经元数量（50%）
jitter_std = 1.0                # 模式脉冲时间抖动标准差 (ms)

# ------------------------------------------------------------------
# SRM 神经元参数（输出神经元）
# ------------------------------------------------------------------
tau_m = 10.0                    # 膜时间常数 (ms)
tau_s = 2.5                     # 突触时间常数 (ms)
T_thresh = 500                  # 发放阈值 (a.u.)
K1 = 2.0                        # 发放后电位快速衰减系数
K2 = 4.0                        # 发放后电位慢速衰减系数
refractory = 1                  # 绝对不应期 (ms)
w_init = 0.475                  # 初始突触权重
w_min, w_max = 0.0, 1.0         # 权重裁剪范围

# ------------------------------------------------------------------
# STDP 参数
# ------------------------------------------------------------------
a_plus = 0.03125                # LTP 学习率
tau_plus = 16.8                 # LTP 时间常数 (ms)
a_minus = 0.85 * a_plus         # LTD 学习率
tau_minus = 33.7                # LTD 时间常数 (ms)
LTP_WINDOW = 7 * tau_plus       # LTP 有效时间窗口
LTD_WINDOW = 7 * tau_minus      # LTD 有效时间窗口

# ------------------------------------------------------------------
# EPSP 核归一化参数（预计算，加速仿真）
# ------------------------------------------------------------------
t_max_epsp = tau_m * tau_s / (tau_m - tau_s) * np.log(tau_m / tau_s)
max_epsp = np.exp(-t_max_epsp / tau_m) - np.exp(-t_max_epsp / tau_s)
K_epsp = 1.0 / max_epsp
decay_m = np.exp(-dt / tau_m)
decay_s = np.exp(-dt / tau_s)

# ------------------------------------------------------------------
# 随机种子
# ------------------------------------------------------------------
np.random.seed(42)
